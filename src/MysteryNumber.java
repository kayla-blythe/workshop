
public class MysteryNumber {

	public static void main(String[] args) {

		int mystery = 100;
		
		mystery += 50;
		
		mystery--;
		
		mystery *= 3;
		
		mystery ++;
		
		mystery /= 2;
		
		mystery += 15;
		
		mystery -= 7;
		
		mystery++;
		
		mystery -= 23;
		
		mystery *= 10;
		
		mystery--;
		
		System.out.println("THE MYSTERY NUMBER = " + mystery);

	}

}
